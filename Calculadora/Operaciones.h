/** \brief Suma de dos enteros
 *
 * \param a int Primer numero ingresado por el usuario
 * \param b int Segundo numero ingresado por el usuraio
 * \return int
 *
 */
int Suma(int a, int b);
/** \brief  Resta de dos enteros
 *
 * \param a int Primer numero ingresado por el usuario
 * \param b int Segundo numero ingresado por el usuraio
 * \return int
 *
 */
int Resta(int a, int b);
/** \brief Division de dos enteros
 *
 * \param a int Primer numero ingresado por el usuario
 * \param b int Segundo numero ingresado por el usuraio
 * \return float
 *
 */
float Division(int a, int b);
/** \brief Multiplicacion de dos enteros
 *
 * \param a int Primer numero ingresado por el usuario
 * \param b int Segundo numero ingresado por el usuraio
 * \return int
 *
 */
int Multiplicacion(int a, int b);
/** \brief  Factoreo del numero que el usuario haya ingresado
 *  no puede ser negativo, ni decimal.
 * \param numero int Numero ingresado por el usuario
 * \return int devuelve el resultado de la operacion
 *
 */
int factorial(int numero);
